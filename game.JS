let levelChosen;

function chooseLevel() {
    let levelChosen = prompt('专/ 砖: 0/1/2/3');
    if (levelChosen >= 0 && levelChosen <= 3)
        return parseInt(levelChosen);
    else {
        alert("专  转拽. 住/ 砖.");
        return chooseLevel();
    }
}



// 驻注转 砖转
